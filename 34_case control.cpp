//case control.
//a-apple
//b-ball
//c-cat
//d-don
#include<stdio.h>
int main()
{
	int ch='d';
	switch(ch){
		case 'a':
			printf("apple\n");
			break;
			
			case 'b':
			printf("ball\n");
			break;
			
			case 'c':
			printf("cat\n");
			break;
			
				case 'd':
			printf("don\n");
			break;
	}
	
	return 0;
}
