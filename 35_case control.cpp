//case control.
//1=hello
//2=welcome
//3=by
//4=thank you
#include<stdio.h>
int main()
{
	int num=4;
	switch(num){
		case 1:
			printf("hello\n");
			break;
			
			case 2:
			printf("welcome\n");
			break;
			
			case 3:
			printf("by\n");
			break;
			
				case 4:
			printf("thank u\n");
			break;
	}
	
	return 0;
}
